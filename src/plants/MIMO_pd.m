function [dx] = MIMO_pd(t, x, u)
    x = x(:);
    x1 = x([1 4]);
    x2 = x([2 5]);
    x3 = x([3 6]);
    
%     f = 0.1*(1-x1.^2)'*x2 + x1;
%     f = x1*x2'*x3;
    f = [0.7*sin(x1(1)).*cos(x1(2));
         0.3.*x1(2)*x2(2)'*x3(2)];
%     f = 0;
%     G11 = sin(x1(1)*x1(2)) + 7;
%     G12 = cos(x2(1)*x2(2)) + 3/2;
%     G22 = sin(x3(1)*x3(2)) + 4;
%     G11 = sin(x1(1))*cos(x1(2)) + 7;
%     G12 = sin(cos((x2(1)*x2(2)))) + 3/2;
%     G22 = cos(x3(1))*sin(x3(2)) + 4;    
    G11 = 1.2 + cos(x1(1)).*sin(x1(2));
    G12 = 0.3*cos(x1(1)*x1(2));
    G22 = 1.7 + sin(x1(1)).*cos(x1(2));
    

    G = [G11 G12;
         G12 G22];
    
    dx = zeros(6, 1);
    dx([1 4]) = x2;
    dx([2 5]) = x3;
    dx([3 6]) = f + G*u;

end

